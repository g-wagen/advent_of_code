data = helper.get_puzzle_input(d=8, y=2021)
data = [x.split(" | ") for x in data]


def easy_digits(out_str):
    counter = 0
    digits = out_str.split(" ")
    for dig in digits:
        foo = np.array([2, 4, 3, 7])
        bar = np.array([len(dig)] * len(foo))
        counter += 1 if (foo == bar).any() else 0
    return counter


end_counter = 0
for d in data:
    end_counter += easy_digits(d[1])

print(f"The digits 1, 4, 7, or 8 appear {end_counter} times.")
